package com.javarush.task.pro.task04.task0405;

/* 
Незаполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int a = 0;
        int b = 0;
        while (a < 2) {
            System.out.println("ББББББББББББББББББББ");
            while (b < 8) {
                System.out.println("Б                  Б");
                b++;
            }
            a++;
        }
    }
}